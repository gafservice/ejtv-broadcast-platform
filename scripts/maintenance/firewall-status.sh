#!/bin/bash

###############################################################################
# EJTV Broadcast Platform
#
# Script.....: ffmpeg-status.sh
# Objetivo...: Diagnóstico completo de FFmpeg
# Autor......: EJTV
###############################################################################

clear

echo "=========================================================="
echo " EJTV Broadcast Platform"
echo " FFmpeg Status"
echo "=========================================================="

echo
echo "Fecha:"
date

echo
echo "Hostname:"
hostnamectl --static

###############################################################################

echo
echo "=========================================================="
echo "1. Binarios"
echo "=========================================================="

which ffmpeg 2>/dev/null || echo "FFmpeg: NO instalado"

which ffprobe 2>/dev/null || echo "FFprobe: NO instalado"

which ffplay 2>/dev/null || echo "FFplay: NO instalado"

###############################################################################

echo
echo "=========================================================="
echo "2. Version"
echo "=========================================================="

if command -v ffmpeg >/dev/null 2>&1
then
    ffmpeg -version | head -5
fi

###############################################################################

echo
echo "=========================================================="
echo "3. Version FFprobe"
echo "=========================================================="

if command -v ffprobe >/dev/null 2>&1
then
    ffprobe -version | head -5
fi

###############################################################################

echo
echo "=========================================================="
echo "4. Version FFplay"
echo "=========================================================="

if command -v ffplay >/dev/null 2>&1
then
    ffplay -version | head -5
fi

###############################################################################

echo
echo "=========================================================="
echo "5. Paquetes instalados"
echo "=========================================================="

dpkg -l | grep -i ffmpeg

###############################################################################

echo
echo "=========================================================="
echo "6. Protocolos principales"
echo "=========================================================="

ffmpeg -protocols 2>/dev/null | grep -Ei \
"rtmp|rtsp|http|https|tcp|udp|srt|srtp"

###############################################################################

echo
echo "=========================================================="
echo "7. Codecs principales"
echo "=========================================================="

ffmpeg -codecs 2>/dev/null | grep -Ei \
"h264|hevc|aac|opus"

###############################################################################

echo
echo "=========================================================="
echo "8. Encoders principales"
echo "=========================================================="

ffmpeg -encoders 2>/dev/null | grep -Ei \
"libx264|libx265|aac|opus"

###############################################################################

echo
echo "=========================================================="
echo "9. Formatos principales"
echo "=========================================================="

ffmpeg -formats 2>/dev/null | grep -Ei \
"flv|mpegts|mp4|matroska|hls"

###############################################################################

echo
echo "=========================================================="
echo "10. Dependencias"
echo "=========================================================="

ldd $(which ffmpeg)

###############################################################################

echo
echo "=========================================================="
echo "11. Auditoria DPKG"
echo "=========================================================="

sudo dpkg --audit

###############################################################################

echo
echo "=========================================================="
echo "12. Espacio en disco"
echo "=========================================================="

df -h /

###############################################################################

echo
echo "=========================================================="
echo " FIN DEL DIAGNOSTICO"
echo "=========================================================="